function parseClubInfo(info) {
	var club = new Object();
	for(var i=0; i<info.length; i++) {
		switch(info[i].Name) {
			case "Brand":
				club.brand=info[i].Value[0].toUpperCase().replace('GOLF', "").trim();
				break;
			case "Model":
				club.model = info[i].Value[0].toUpperCase();
				break;
			case "Club Type":
				club.type =  info[i].Value[0].toUpperCase();
				break;
			case "Shaft Material":
				club.shaft = info[i].Value[0].toUpperCase();
				break;
			default:
				//console.log("Unknown Info Property: " + info[i].Name + ": " + info[i].Value[0]);
				break;
		}
	}
	return club;
};

